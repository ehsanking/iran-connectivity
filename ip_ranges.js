// Helper function to generate individual IPs from CIDR ranges
function cidrToIps(cidr) {
    const [ip, prefix] = cidr.split('/');
    const ipNum = ipToNumber(ip);
    const mask = Math.pow(2, 32 - parseInt(prefix)) - 1;
    const network = ipNum & ~mask;
    const broadcast = network | mask;
    
    const ips = [];
    for (let i = network + 1; i < broadcast; i++) {
        ips.push(numberToIp(i));
    }
    return ips;
}

function ipToNumber(ip) {
    const parts = ip.split('.');
    return parts.reduce((acc, part, index) => {
        return acc + (parseInt(part) << (8 * (3 - index)));
    }, 0);
}

function numberToIp(num) {
    return [
        (num >>> 24) & 255,
        (num >>> 16) & 255,
        (num >>> 8) & 255,
        num & 255
    ].join('.');
}

// Function to get all IP ranges for a specific category
function getIpRanges(category) {
    if (IP_RANGES[category]) {
        return IP_RANGES[category].ranges;
    }
    return [];
}

// Function to get all categories
function getAllCategories() {
    return Object.keys(IP_RANGES);
}

// Function to get provider information
function getProviderInfo(category) {
    return IP_RANGES[category] || null;
}

// Function to get all providers with their names
function getAllProviders() {
    const providers = [];
    for (const [key, value] of Object.entries(IP_RANGES)) {
        providers.push({
            key: key,
            name: value.name,
            rangeCount: value.ranges.length
        });
    }
    return providers;
}

